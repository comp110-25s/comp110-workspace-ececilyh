"""File to define Bear class."""


class Bear:
    age: int
    hunger_score: int

    def __init__(self):
        return None

    def one_day(self) -> None:
        hunger_score -= 1
        age += 1

    def eat(self, num_fish: int) -> None:
        hunger_score += num_fish

    def __init__(self):
        self.age = 0
        self.hunger_score = 0
